export const getServiceExplanation = (serviceType, language = 'en') => {
  // Default to English if the requested language is not available
  const lang = serviceDescriptions[language] ? language : 'en';
  const service = serviceDescriptions[lang][serviceType];
  
  if (!service) return "";

  if (lang === 'hi') {
    return `${service.introduction} 
      आपको क्या करना होगा: ${service.requirements.join("। ")}।
      
      प्रक्रिया: ${service.process.join("। ")}।
      
      महत्वपूर्ण: ${service.notes}`;
  }

  return `${service.introduction} 
    Here's what you need to do: ${service.requirements.join(". ")}
    
    Process: ${service.process.join(". ")}
    
    Important: ${service.notes}`;
};

export const getServiceDetails = (serviceType, language = 'en') => {
  // Default to English if the requested language is not available
  const lang = serviceDescriptions[language] ? language : 'en';
  return serviceDescriptions[lang][serviceType] || null;
};
